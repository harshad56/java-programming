
public class PrimitiveType {
    public static void main(String[] args) {
        int a = 10;// here int
        byte age = 98;// here byte goto -128 to 127 value
        short s = 32767;// here short goto -32768 to 32767 value

        long l = 123334454565676L;// here you can write long values but end with write L
        float f = 12.37f;// here you can write float values but end with write f
        double d = 12.3734D;// here you can write double values but end with
                            // write D or not compulsory write D
        char ch = 'a';

        System.out.println("a:" + a);
        System.out.println("age:" + age);
        System.out.println(s);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);

    }
}
