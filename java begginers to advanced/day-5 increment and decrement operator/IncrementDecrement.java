
public class IncrementDecrement {
    public static void main(String[] args) {
        int a = 10;// first a(10)value assined in b then it increment in b(1)
        a++; // 10+1=(11)
        int b = a; // b=11

        int c = 34;
        int d = ++c;// first increment goes in d(1) then c value(34) assigend in d ,1+34=35

        System.out.println(b);

        System.out.println(d);

        // a--,--a(decrement)work same like increment operator but do minus work
    }
}
