public class NumberInWord {

    public static void main(String[] args) {

        int number = 0;
        switch (number) {
            case 0:
                System.out.println("zero");
                break;

            case 1:
                System.out.println("one");
                break;

            case 2:
                System.out.println("two");
                break;
        }

    }
}
